export default function BottomNav() {
  const location = useLocation()
  const navigate = useNavigate()

  const isActive = (path) => {
    if (path === '/') return location.pathname === '/'
    return location.pathname.startsWith(path)
  }

  return (
    <nav className="app-bottom-nav fixed bottom-0 left-0 right-0 z-50 safe-bottom md:hidden">
      <div className="max-w-lg mx-auto">
        <div className="mx-3 mb-2 bg-white/90 backdrop-blur-xl rounded-2xl shadow-elevated border border-white/50">
          <div className="flex items-center justify-around h-16">
            {tabs.map(tab => {
              const active = isActive(tab.path)
              return (
                <button key={tab.path} onClick={() => { navigate(tab.path); window.scrollTo(0, 0) }} className="flex flex-col items-center justify-center min-w-[56px] min-h-tap gap-0.5 relative group">
                  <div className={`transition-all duration-300 ${active ? 'text-tree-600 scale-110' : 'text-gray-400 group-hover:text-gray-600'}`}>
                    {tab.icon(active)}
                  </div>
                  <span className={`text-[10px] font-semibold transition-colors duration-200 ${active ? 'text-tree-600' : 'text-gray-400 group-hover:text-gray-600'}`}>{tab.label}</span>
                  {active && <div className="absolute -bottom-1 w-5 h-0.5 rounded-full bg-tree-500" />}
                </button>
              )
            })}
          </div>
        </div>
      </div>
    </nav>
  )
}
